<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;
use Illuminate\Http\Request;
use App\Models\Ingredient;
use Livewire\WithPagination;


class IngredientController extends Controller
{

    use WithPagination;

    public function __construct(){
        $this->middleware('can:admin.ingredients.index')->only('index');
        $this->middleware('can:admin.ingredients.create')->only('create', 'store');
        $this->middleware('can:admin.ingredients.edit')->only('edit', 'update');
        $this->middleware('can:admin.ingredients.destroy')->only('destroy');
    }

    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        return view('admin.ingredients.index', [
            'ingredients' => Ingredient::all()
        ]);

        return view('admin.ingredients.index');
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('admin.ingredients.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
        ]);

        $ingredient = Ingredient::create($request->all());

        return redirect()->route('admin.ingredients.index', compact('ingredient'))
            ->with('success', 'El ingrediente se creo con exito');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show(Ingredient $ingredient)
    {
        return view('admin.ingredients.show', compact('ingredient'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit(Ingredient $ingredient)
    {
        return view('admin.ingredients.edit', compact('ingredient'))->with('success', 'El ingrediente se actualizo con exito');
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request,Ingredient $ingredient)
    {
        $request->validate([
            'name' => 'required',
        ]);

        $ingredient->update($request->all());

        return redirect()->route('admin.ingredients.index', compact('ingredient'))
            ->with('success', 'El ingrediente se actualizo con exito');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Ingredient $ingredient)
    {
        $ingredient->delete();

        return redirect()->route('admin.ingredients.index')
            ->with('info', 'El ingrediente se elimino con exito');
    }
}
